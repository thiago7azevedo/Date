package course;

public class Program7 {
	public static void main(String[] args) {

		int a = 10;
		a++;
		System.out.println(a);
		System.out.println();
		
		//----------------
		
		int b = 10;
		int c = b++;
		
		System.out.println(b);
		System.out.println(c);
		
		System.out.println();
		//-----------------
		
		int d = 10;
		int e = ++d;
		
		System.out.println(d);
		System.out.println(e);

	}

}